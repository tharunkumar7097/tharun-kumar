num_1=int(input("Enter first number"))
num_2=int(input("Enter second number"))
operator=input("Enter the Arithmetic operator:")
if operator== "+":
    print("The sum of",num_1,"and",num_2,"is:",num_1+num_2)
if operator== "-":
    print("The difference of",num_1,"and",num_2,"is:",num_1-num_2)
if operator== "*":
    print("The product of",num_1,"and",num_2,"is:",num_1*num_2)
if operator== "/":
    print("The quotient of",num_1,"and",num_2,"is:",num_1//num_2)
if operator== "%":
    print("The remainder of",num_1,"and",num_2,"is:",num_1%num_2)